<h2 align=center> ТАБЛО ДЛЯ АЗС</h2> <br>
<p align="justify">
Электронные табло для АЗС предназначены для отображения  стоимости видов топлива. Дополнительно возможно отображение температуры  воздуха, атмосферного давления, радиационного фона или относительной влажности  воздуха в любых сочетаниях. Электронное табло  АЗС имеет герметичный корпус, акриловое стекло-светофильтр,  управляющую электронику (контроллер или регистр управления), источник питания,  необходимые кабеля.<br> Имеется возможность исполнение табло цен АЗС без использования остекления  и декоративного профиля (без корпуса), что позволяет снизить стоимость.  Подобные табло АЗС необходимо устанавливать в герметичные стелы АЗС. 
</p>
<p>Управление табло  АЗС происходит посредством пульта дистанционного управления на  ИК лучах (ПДУ), пульта с ЖКИ (интерфейс RS-232) или компьютера под управлением  ОС Windows (интерфейс RS-232), что позволяет корректировать цены топлива и  производить необходимые настройки. 
</p>
<p>Питание электронных табло  осуществляется от сети переменного тока 220В /  50Гц. Табло имеет резервный источник питания, который позволяет сохранять все  настройки на случай отключения питания до 1 года.
</p>
<p>Табло  для использования внутри помещения , около окна оператора или при входе в АЗС с  часами-термометром. Цвет индикации КРАСНЫЙ или ЗЕЛЕНЫЙ.<br></p>
