<h2 align=center> ТАБЛО КУРСОВ ВАЛЮТ</h2> <br>
<p align="justify">
  <p>Табло курсов обмена валют предназначены для индикации курсов  обмена валюты и применяются в банках и пунктах обмена валют. Элементы индикации  изготавливаются с использованием светодиодов или светодиодных матриц.  Управление табло - пульт ДУ на ИК лучах и компьютер (кабель связи 10 метров и программное  обеспечение входят в комплект поставки). <br />
Электронные  табло обмена валют кроме самого курса валют , по желанию заказчика, могут отображать время,  дату и температуру воздуха.<br />
Возможно дополнительное изготовление логотипа. <br />
Изготовление табло с большим количеством валют и индикаторов  в ячейке – по индивидуальному заказу.<br />
Питание электронного табло обмена валют осуществляется от сети переменного тока 220В/50Гц. В случае отключения внешнего  питания, все значения (курсов валют, времени, даты) и пользовательские  настройки электронного валютного табло сохраняются в памяти до 1  года.<br />
Гарантия на электронное табло  курсов валют составляет 12 месяцев, а срок службы на которое  рассчитаны подобные табло валют — до 10 лет.<br />
Габаритные размеры указаны ориентировочно, в каждом  конкретном случае возможно изменение габаритных размеров, цвета фоновой пленки  и надписей.</p>

