Нами производятся серии табло «бегущая строка»  для использования как внутри помещений, так и на улице.<br />
Стандартно выпускаемые  табло могут иметь разрешение по вертикали 8 , 16, 24 или 32 пикселя (точек). <br />
Выпускаемые нами табло обладают большим набором функциональных  возможностей и развитым набором программного обеспечения поставляемого с табло.  В составе табло имеются встроенные часы-календарь и энергонезависимая память  которые сохраняют ход времени и введенную информацию при отключении табло от  сети. Также,  дополнительно, в табло возможна  установка датчиков температуры , атмосферного давления и относительной  влажности воздуха и радиационного фона. <br />
Программа редактор и загрузчик шрифтов позволяет  создавать и редактировать в дополнение к стандартным пользовательские шрифты,  позволяя без особых затруднений выводить на табло собственные шрифты, фирменные  знаки и логотипы. <br />
Стандартное управление Табло «Бегущая строка»  осуществляется с помощью компьютера, необходимого только в момент изменения  информации в табло. <br>
