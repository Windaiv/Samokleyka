<p align="justify">
Одно из самых эффективных средств рекламы и информации на сегодняшний день. Яркая, динамично меняющаяся информация на  электронном светодиодном табло привлекает внимание клиентов и позволяет донести  до потребителя максимальный объем информации, используя минимум рекламного  пространства. Любые габаритные размеры, различные цветовые решения и  многочисленные спецэффекты - все это дает неограниченные возможности для  эффективного рекламно-информационного применения табло "Бегущая строка".
